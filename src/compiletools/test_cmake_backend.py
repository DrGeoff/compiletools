class TestCppmPerSourceProperties:
    """Test that .cppm sources get per-source set_source_files_properties and
    -x is NOT in global target_compile_options.

    Background: gcc injects ``-x c++`` immediately before the .cppm filename
    so it recognises the extension as C++ source.  build_backend.extract_copts
    keeps the orphan ``-x`` (``c++`` is not flag-shaped) and aggregate_rule_sources
    deduplicates it into all_copts.  cmake's target_compile_options then emits
    that orphan at global scope; gcc interprets the next token (cmake's own
    ``-MD``) as the language argument and raises:
        g++: error: language -MD not recognized
    Fix: filter ``-x`` from global copts, emit per-source
    set_source_files_properties for each .cppm file.
    """

    def _make_backend(self):
        args = MagicMock()
        hunter = MagicMock()
        return CMakeBackend(args=args, hunter=hunter)

    def _generate(self, graph):
        """Build a CMakeBackend, run generate on *graph*, return content."""
        backend = self._make_backend()
        buf = io.StringIO()
        backend.generate(graph, output=buf)
        return buf.getvalue()

    def _build_graph_with_cppm(self, cppm_path: str = "math.cppm") -> BuildGraph:
        """Return a minimal BuildGraph: one .cppm compile + one link rule."""
        graph = BuildGraph()
        # The compile command mirrors what build_backend._create_compile_rule emits
        # for a gcc .cppm file: -x c++ placed immediately before the filename.
        graph.add_rule(
            BuildRule(
                output="obj/math.o",
                inputs=[cppm_path],
                command=[
                    "g++",
                    "-std=c++20",
                    "-fmodules-ts",
                    "-c",
                    "-x",
                    "c++",
                    cppm_path,
                    "-o",
                    "obj/math.o",
                ],
                rule_type="compile",
            )
        )
        graph.add_rule(
            BuildRule(
                output="bin/math",
                inputs=["obj/math.o"],
                command=["g++", "-o", "bin/math", "obj/math.o"],
                rule_type="link",
            )
        )
        return graph

    def test_cppm_has_per_source_compile_options(self):
        """set_source_files_properties must appear for the .cppm source."""
        graph = self._build_graph_with_cppm("math.cppm")
        content = self._generate(graph)

        assert "set_source_files_properties" in content
        assert "math.cppm" in content
        # The per-source block must contain COMPILE_OPTIONS and LANGUAGE CXX
        assert "COMPILE_OPTIONS" in content
        assert "LANGUAGE CXX" in content

    def test_cppm_per_source_contains_x_cxx(self):
        """The per-source COMPILE_OPTIONS must encode -x;c++ (cmake list separator)."""
        graph = self._build_graph_with_cppm("math.cppm")
        content = self._generate(graph)

        # Check that -x and c++ appear in the set_source_files_properties block
        lines = content.splitlines()
        ssf_lines = [ln for ln in lines if "set_source_files_properties" in ln or "COMPILE_OPTIONS" in ln]
        combined = "\n".join(ssf_lines)
        assert "-x" in combined
        assert "c++" in combined

    def test_global_target_compile_options_no_x_flag(self):
        """The orphan -x must NOT appear in global target_compile_options."""
        graph = self._build_graph_with_cppm("math.cppm")
        content = self._generate(graph)

        lines = content.splitlines()
        tco_lines = [ln for ln in lines if "target_compile_options" in ln]
        for ln in tco_lines:
            assert '"-x"' not in ln, f"Orphan -x found in global target_compile_options: {ln!r}"

    def test_regular_cpp_no_per_source_properties(self):
        """Regular .cpp sources must NOT trigger set_source_files_properties."""
        graph = BuildGraph()
        graph.add_rule(
            BuildRule(
                output="obj/main.o",
                inputs=["main.cpp"],
                command=["g++", "-std=c++20", "-c", "main.cpp", "-o", "obj/main.o"],
                rule_type="compile",
            )
        )
        graph.add_rule(
            BuildRule(
                output="bin/main",
                inputs=["obj/main.o"],
                command=["g++", "-o", "bin/main", "obj/main.o"],
                rule_type="link",
            )
        )
        content = self._generate(graph)

        assert "set_source_files_properties" not in content


class TestCMakeNamedModuleIfaceLinkWiring:
    """Named-module interface units (.cppm) are pre-compiled locally by
    ``_prebuild_aux_artefacts`` before cmake runs. They MUST be excluded
    from ``add_executable`` srcs and instead supplied to the linker as
    prebuilt object files.

    Race rationale: the interface compile command writes the BMI (.gcm)
    to a fixed mapper-resolved path inside cas-pcmdir. If cmake re-compiles
    the .cppm file (because it appears in add_executable srcs), the gcc
    invocation truncates and rewrites that .gcm path while a peer importer
    compile in the same cmake --parallel invocation may simultaneously
    open it for reading. Race symptom (observed on CI gcc-14.2.0 / make
    generator): ``math: error: failed to read compiled module: No such
    file or directory``. The prebuild already produced the .o + .gcm;
    cmake must consume them, not re-produce them.
    """

    def _make_backend(self):
        args = MagicMock()
        hunter = MagicMock()
        return CMakeBackend(args=args, hunter=hunter)

    def _build_graph_with_module_iface(self) -> BuildGraph:
        """Importer main.cpp + named-module interface math.cppm linked together."""
        graph = BuildGraph()
        graph.add_rule(
            BuildRule(
                output="/abs/cas-objdir/ab/math_abcd.o",
                inputs=["/abs/ws/math.cppm"],
                command=[
                    "g++",
                    "-std=c++20",
                    "-fmodules-ts",
                    "-c",
                    "-x",
                    "c++",
                    "/abs/ws/math.cppm",
                    "-o",
                    "/abs/cas-objdir/ab/math_abcd.o",
                ],
                rule_type="compile",
            )
        )
        graph.add_rule(
            BuildRule(
                output="/abs/cas-objdir/cd/main_efgh.o",
                inputs=["/abs/ws/main.cpp"],
                command=[
                    "g++",
                    "-std=c++20",
                    "-fmodules-ts",
                    "-c",
                    "/abs/ws/main.cpp",
                    "-o",
                    "/abs/cas-objdir/cd/main_efgh.o",
                ],
                rule_type="compile",
            )
        )
        graph.add_rule(
            BuildRule(
                output="bin/main",
                inputs=["/abs/cas-objdir/ab/math_abcd.o", "/abs/cas-objdir/cd/main_efgh.o"],
                command=["g++", "-o", "bin/main", "/abs/cas-objdir/ab/math_abcd.o", "/abs/cas-objdir/cd/main_efgh.o"],
                rule_type="link",
            )
        )
        return graph

    def test_cppm_not_in_add_executable_srcs(self):
        """The .cppm source must NOT appear in add_executable srcs."""
        graph = self._build_graph_with_module_iface()
        backend = self._make_backend()
        # Mark math.cppm's .o as a named-module interface producer; the
        # backend must keep that .o out of the compile target's srcs.
        backend._module_iface_obj = {"math": "/abs/cas-objdir/ab/math_abcd.o"}
        buf = io.StringIO()
        backend.generate(graph, output=buf)
        content = buf.getvalue()

        # main.cpp is a regular importer, still compiled by cmake.
        assert "/abs/ws/main.cpp" in content
        # math.cppm is a named-module interface — already compiled by the
        # prebuild step; cmake must not list it as a source.
        assert "math.cppm" not in content, (
            "math.cppm leaked into the cmake target srcs; cmake will re-compile "
            "it and race with the prebuild-produced .gcm"
        )

    def test_prebuilt_module_iface_obj_linked_in(self):
        """The prebuilt interface .o must be passed to the linker so the
        module's definitions are present in the final binary."""
        graph = self._build_graph_with_module_iface()
        backend = self._make_backend()
        backend._module_iface_obj = {"math": "/abs/cas-objdir/ab/math_abcd.o"}
        buf = io.StringIO()
        backend.generate(graph, output=buf)
        content = buf.getvalue()

        # The prebuilt .o path must be supplied to the linker. cmake
        # passes absolute paths in target_link_libraries directly to ld.
        assert "/abs/cas-objdir/ab/math_abcd.o" in content, (
            "prebuilt named-module interface .o is not wired into the linker; "
            "the executable would link without the module's definitions and "
            "fail with 'undefined reference'"
        )

    def test_no_module_iface_obj_leaves_graph_unchanged(self):
        """Backends without named-module producers (the common case) must
        keep their previous behaviour: .cppm sources stay in add_executable
        srcs and no spurious prebuilt-object link entries appear."""
        graph = self._build_graph_with_module_iface()
        backend = self._make_backend()
        # Empty _module_iface_obj — no named modules.
        buf = io.StringIO()
        backend.generate(graph, output=buf)
        content = buf.getvalue()

        assert "math.cppm" in content


@pytest.mark.skipif(shutil.which("cmake") is None, reason="cmake not on PATH")
class TestCMakeRunsTestsInBuildPhase:
    """CMakeBackend.execute("build") runs each test via its runtests-target
    add_custom_command, so ``cmake --build`` runs the test as part of the
    build — no separate post-build ``runtests`` phase.
    """

    @pytest.fixture(autouse=True)
    def _reset_parser_state(self):
        uth.reset()
        yield
        uth.reset()

    @pytest.fixture(autouse=True)
    def _chdir_to_tmp(self, monkeypatch, tmp_path):
        monkeypatch.chdir(tmp_path)

    @uth.requires_functional_compiler
    def test_cmake_runs_tests_in_build(self, tmp_path):
        """After execute("build") — NOT execute("runtests") — the test's
        ``.result`` success marker exists at the graph-declared path, proving
        the test ran during the build phase and the marker landed where the
        rest of compiletools expects it (not buried inside cmake-build/)."""
        (tmp_path / "unit_test.hpp").write_text("#pragma once\n")
        test_src = tmp_path / "test_pass.cpp"
        test_src.write_text('#include "unit_test.hpp"\nint main() { return 0; }\n')

        backend, graph = uth.build_real_backend(CMakeBackend, tmp_path, [], tests=[test_src])
        with open(tmp_path / "CMakeLists.txt", "w") as f:
            backend.generate(graph, output=f)

        backend.execute("build")

        markers = uth.find_result_markers(tmp_path)
        assert markers, "no .result marker after execute('build') — test did not run during the build phase"
        # add_custom_command resolves a relative OUTPUT against the build dir;
        # the marker must be anchored to the source tree, not stranded under
        # cmake-build/.
        assert not any("cmake-build" in m for m in markers), (
            f".result marker landed inside cmake-build/ instead of the graph path: {markers}"
        )
        test_rule = next(r for r in graph.rules if r.rule_type == "test")
        marker = test_rule.success_marker
        assert marker is not None
        assert os.path.exists(os.path.join(tmp_path, marker)), (
            f"marker not at the graph-declared path {marker}; found {markers}"
        )

    @uth.requires_functional_compiler
    def test_cmake_test_failure_fails_build(self, tmp_path):
        """A failing test must make execute("build") raise CalledProcessError,
        and the failing test's ``.result`` marker must NOT be created (the
        touch only runs after the test exits 0)."""
        (tmp_path / "unit_test.hpp").write_text("#pragma once\n")
        test_src = tmp_path / "test_fail.cpp"
        test_src.write_text('#include "unit_test.hpp"\nint main() { return 1; }\n')

        backend, graph = uth.build_real_backend(CMakeBackend, tmp_path, [], tests=[test_src])
        with open(tmp_path / "CMakeLists.txt", "w") as f:
            backend.generate(graph, output=f)

        with pytest.raises(subprocess.CalledProcessError):
            backend.execute("build")

        results = uth.find_result_markers(tmp_path)
        assert not results, f"failing test left a .result marker (touch ran despite rc!=0): {results}"
