        expected_key = hashlib.blake2b(f"ROOT|{rel_src}".encode(), digest_size=6).hexdigest()
        assert bd.endswith(f"cmake-build-{expected_key}"), f"expected sentinel-ROOT key {expected_key!r}; got {bd!r}"

